package com.capmkts.msrprocess.data;

public class CommitmentDetails {

	private String commitmentNumber;
	private String commitmentDate;
	private String commitmentExprDate;
	private String dataDeliveryDate;
	private String targetFundingDate;
	private String remitTypeDescription;
	private String unpaidPrincipalBalance;
	private String loanCount;

	public String getCommitmentNumber() {
		return commitmentNumber;
	}
	public void setCommitmentNumber(String commitmentNumber) {
		this.commitmentNumber = commitmentNumber;
	}
	public String getCommitmentDate() {
		return commitmentDate;
	}
	public void setCommitmentDate(String commitmentDate) {
		this.commitmentDate = commitmentDate;
	}
	public String getCommitmentExprDate() {
		return commitmentExprDate;
	}
	public void setCommitmentExprDate(String commitmentExprDate) {
		this.commitmentExprDate = commitmentExprDate;
	}
	public String getDataDeliveryDate() {
		return dataDeliveryDate;
	}
	public void setDataDeliveryDate(String dataDeliveryDate) {
		this.dataDeliveryDate = dataDeliveryDate;
	}
	public String getTargetFundingDate() {
		return targetFundingDate;
	}
	public void setTargetFundingDate(String targetFundingDate) {
		this.targetFundingDate = targetFundingDate;
	}
	public String getRemitTypeDescription() {
		return remitTypeDescription;
	}
	public void setRemitTypeDescription(String remitTypeDescription) {
		this.remitTypeDescription = remitTypeDescription;
	}
	public String getUnpaidPrincipalBalance() {
		return unpaidPrincipalBalance;
	}
	public void setUnpaidPrincipalBalance(String unpaidPrincipalBalance) {
		this.unpaidPrincipalBalance = unpaidPrincipalBalance;
	}
	public String getLoanCount() {
		return loanCount;
	}
	public void setLoanCount(String loanCount) {
		this.loanCount = loanCount;
	}

	
}
